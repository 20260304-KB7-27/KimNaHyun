<template>
  <!-- songsлҘј PropsлЎң м „лӢ¬ -->
  <SongList :songs="songs">You did it!</SongList>
  <button @click="addSong">мғҲлЎңмҡҙ кіЎ м¶”к°Җ</button>
</template>

<script setup>
import { ref, provide, computed } from 'vue';
import SongList from './components/SongList.vue';

const songs = ref([
  { id: 1, title: 'Blueming', done: true },
  { id: 2, title: 'Dynamite', done: true },
  { id: 3, title: 'Lovesick Girls', done: false },
  { id: 4, title: 'л§ҲлҰ¬м•„(Maria)', done: false },
]);

// songs м¶”к°Җ
const addSong = () => {
  songs.value.push({
    id: 1,
    title: 'Blueming',
    done: true,
  });
};
// Provide
// - provide('нӮӨ', лҚ°мқҙн„°) : м •м Ғ к°’
provide('icons', {
  checked: 'far fa-check-circle',
  unchecked: 'far fa-circle',
});

// - provide('нӮӨ', computed()) : л°ҳмқ‘нҳ•мқ„ мң м§Җн•ң лҚ°мқҙн„°
provide(
  'doneCount',
  computed(() => songs.value.filter((s) => s.done === true).length),
);
</script>
